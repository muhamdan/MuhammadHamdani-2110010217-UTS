    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // mengambil nilai
        String updetdistributor = cbDistributor.getSelectedItem().toString();
        String updetnamabarang = tfBarang.getText();
        double updetstok = Double.parseDouble(tfStok.getText());
        double updetharga = Double.parseDouble(tfHarga.getText());
        double updetmodal = Double.parseDouble(tfModal.getText());
        // memperbaharui nilai
        baranglist.get(row).distributor = updetdistributor;
        baranglist.get(row).namabarang = updetnamabarang;
        baranglist.get(row).stok = updetstok;
        baranglist.get(row).harga = updetharga;
        baranglist.get(row).modal = updetmodal;
        //reset table and repopulated
        dtm.setRowCount(0);
        for (int i = 0; i < baranglist.size(); i++) {
            Object[] objs = {baranglist.get(i).distributor,baranglist.get(i).namabarang,baranglist.get(i).stok,baranglist.get(i).harga,baranglist.get(i).modal};
       
            dtm.addRow(objs);
        }
        
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this,"Hapus Data ini ?", "Hapus", dialogButton);
        if(dialogResult == 0){
        // remove
            dtm.removeRow(row);
            baranglist.remove(row);
            dtm.setRowCount(0);
            for (int i = 0; i < baranglist.size(); i++) {
            Object[] objs = {baranglist.get(i).namabarang,baranglist.get(i).distributor,
                baranglist.get(i).stok,baranglist.get(i).harga,baranglist.get(i).modal};
            dtm.addRow(objs);
        }
        //menjalankan method clearfield
            clearField();
        } else {
            
        }
    }//GEN-LAST:event_btnHapusActionPerformed

