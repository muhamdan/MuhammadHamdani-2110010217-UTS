    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
    // ketika klik tombol tambah
    //mengambil nilai dari teks field
        String namabarang = tfBarang.getText();
        String distributor = cbDistributor.getSelectedItem().toString();
        double stok = Double.parseDouble(tfStok.getText());
        double harga = Double.parseDouble(tfHarga.getText());
        double modal = Double.parseDouble(tfModal.getText());
    //memanggil objek
        baranglist.add(new Barang(namabarang,distributor,stok,harga,modal));
    //reset data model
        dtm.setRowCount(0);
    //untuk mengisi tabel
        for (int i = 0; i < baranglist.size(); i++) {
            Object[] objs = {baranglist.get(i).distributor,baranglist.get(i).namabarang,baranglist.get(i).stok,baranglist.get(i).harga,baranglist.get(i).modal};
    //menambahkan data model
            dtm.addRow(objs);
        }
    //reset
    clearField();
    }//GEN-LAST:event_btnTambahActionPerformed

