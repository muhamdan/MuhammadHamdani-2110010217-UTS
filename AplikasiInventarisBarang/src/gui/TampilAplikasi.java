    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        String input = JOptionPane.showInputDialog(this, "Cari Nama Barang :");
        for (int i = 0; i < baranglist.size(); i++) {
            if (baranglist.get(i).namabarang.equalsIgnoreCase(input)){
                JOptionPane.showMessageDialog(btnCari, "Ditemukan!!","Cari Barang",2);
                tfBarang.setText(baranglist.get(i).namabarang);
                tfStok.setText(String.valueOf(baranglist.get(i).stok));
                tfHarga.setText(String.valueOf(baranglist.get(i).harga));
                tfModal.setText(String.valueOf(baranglist.get(i).modal));
                String distributor = baranglist.get(i).distributor;
                 for (int j = 0; j<(cbDistributor.getItemCount());j++){
                     if (cbDistributor.getItemAt(i).equalsIgnoreCase(distributor)){
                         cbDistributor.setSelectedIndex(i);
                     }
                 }
                 return; // Quit after Found
            }
        }
        JOptionPane.showMessageDialog(btnCari, "Tidak Ditemukan !!!", "Cari Barang",2);
    }//GEN-LAST:event_btnCariActionPerformed

